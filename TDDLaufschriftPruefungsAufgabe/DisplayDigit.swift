    private func drawLetterWithCharacter(character: Character, atPosition: Int, withOffset:Bool) {
        
        let code = codeForCharacter(character)
        let xPos = atPosition * 5 * width
        
        func addLineAtPosition(yPos: Int,rowdescription:[Int]) {
            for (line, codeBit) in rowdescription.enumerate() {
                
                let element = UIView(frame: CGRect(x: xPos + line*width, y: yPos, width: width, height: height))
                element.backgroundColor = codeBit == 1 ? UIColor.iKVBDisplayDigitOn() :  UIColor.iKVBDisplayDigitOff().colorWithAlphaComponent(0.8)
                element.layer.cornerRadius = cornerRadius
                self.addSubview(element)
            }
        }
        
        if withOffset {
            addLineAtPosition(height,rowdescription: [0,0,0,0,0])
        }
        for (zeile,code) in code.enumerate() {
            // let code = [1,1,1,1,1,1]
            if withOffset {
                addLineAtPosition((zeile+1)*height,rowdescription: code)
            }
             else {
                addLineAtPosition(zeile*height,rowdescription: code)
            }
        }
    }

